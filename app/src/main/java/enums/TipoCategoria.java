
package enums;

/**
 * <h3>Clase Enum TipoCategoria</h3>
 * <p>Enumeración que representa los tipos de categoría para clasificar movimientos financieros.
 * Puede ser de tipo INGRESO o GASTO.</p>
 * <p>Esta enumeración permite clasificar los movimientos financieros en dos categorías principales:</p>
 * <ul>
 *  <li>INGRESO, que representa ingresos financieros recibidos.</li>
 *  <li>GASTO, que representa gastos financieros realizados.</li>
 * </ul>
 * @author Grupo1
 */
public enum TipoCategoria {
    /**
     * Tipo de categoría para ingresos financieros recibidos.
     */
    INGRESO,
    
    /**
     *Tipo de categoría para gastos financieros realizados.
     */
    GASTO;
}
