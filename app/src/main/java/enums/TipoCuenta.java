
package enums;

/**
 *  <h3>Clase Enum TipoCuenta</h3>
 * <p>Enumeración que representa los tipos de cuenta bancaria disponibles.
 * Puede ser de tipo AHORRO o CORRIENTE.</p>
 * <p>Esta enumeración permite distinguir entre diferentes tipos de cuentas bancarias
 * según su funcionalidad y características.</p>
 * @author Grupo1 
 */
public enum TipoCuenta {
    /**
     * Tipo de cuenta bancaria de ahorro.
     */
    AHORRO,
    
    /**
     * Tipo de cuenta bancaria corriente.
     */
    CORRIENTE;
}
