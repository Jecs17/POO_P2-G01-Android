
package enums;

/**
 *  <h3>Clase Enum TipoID</h3>
 * <p>Enumeración que representa los tipos de identificación personal disponibles.
 * Puede ser de tipo CEDULA o RUC.</p>
 * <p>Esta enumeración permite distinguir entre diferentes tipos de documentos de
 * identificación personal utilizados en varios contextos legales y comerciales.</p>
 * @author Grupo1
 */
public enum TipoID {
    /**
     * Tipo de identificación personal tipo cédula.
     */
    CEDULA,
    
    /**
     * Tipo de identificación personal tipo RUC.
     */
    RUC;
}
